CREATE TABLE site_bibliographic_reference (
  site_id INTEGER NOT NULL REFERENCES site(site_id) ON DELETE CASCADE,
  bibliographic_reference_id INTEGER NOT NULL REFERENCES bibliographic_reference(bibliographic_reference_id) ON DELETE CASCADE,
  PRIMARY KEY (site_id, bibliographic_reference_id)
);

CREATE TABLE archaeological_context_bibliographic_reference (
  archaeological_context_id INTEGER NOT NULL REFERENCES archaeological_context(archaeological_context_id) ON DELETE CASCADE,
  bibliographic_reference_id INTEGER NOT NULL REFERENCES bibliographic_reference(bibliographic_reference_id) ON DELETE CASCADE,
  PRIMARY KEY (archaeological_context_id, bibliographic_reference_id)
);

CREATE TYPE burial_context_enum AS ENUM ('YES', 'NO', 'POSSIBLE', 'UNCERTAIN');
CREATE TYPE burial_type_enum AS ENUM ('PRIMARY_SINGLE_BURIAL', 'PRIMARY_DOUBLE_BURIAL', 'PRIMARY_TRIPLE_BURIAL', 'PRIMARY_MULTIPLE_BURIAL', 'SECONDARY_SINGLE_BURIAL', 'SECONDARY_DOUBLE_BURIAL', 'SECONDARY_TRIPLE_BURIAL', 'SECONDARY_MULTIPLE_BURIAL');

CREATE TABLE funerary_context (
  funerary_context_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  archaeological_context_id INTEGER NOT NULL REFERENCES archaeological_context(archaeological_context_id) ON DELETE CASCADE,
  burial_context burial_context_enum NOT NULL DEFAULT 'UNCERTAIN',
  burial_type burial_type_enum,
  ochre_presence BOOLEAN,
  grave_goods BOOLEAN,
  notes TEXT,
  updated_at timestamptz NOT NULL DEFAULT now(),
    CONSTRAINT chk_funerary_context_type CHECK (
      (burial_context = 'NO' AND burial_type IS NULL)
      OR
      (burial_context = 'YES' AND burial_type IS NOT NULL)
      OR
      (burial_context IN ('POSSIBLE', 'UNCERTAIN'))
    )
);

CREATE TABLE funerary_context_individual (
  funerary_context_id INTEGER NOT NULL REFERENCES funerary_context(funerary_context_id) ON DELETE CASCADE,
  individual_id INTEGER NOT NULL REFERENCES individual(individual_id) ON DELETE CASCADE,
  PRIMARY KEY (funerary_context_id, individual_id)
);

CREATE OR REPLACE FUNCTION set_updated_at()
RETURNS trigger AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

